nilai = int (input ("Masukkan Nilai :"))
if nilai >= 90 :
    print ("> nilai %s = 'A' " %(nilai))
elif nilai >= 80 :
    print ("> nilai %s = 'B' " %(nilai))
elif nilai >= 70 :
    print ("> nilai %s = 'C' " %(nilai))
elif nilai >= 60 :
    print ("> nilai %s = 'D' " %(nilai))
elif nilai >= 40 :
    print ("> nilai %s = 'E' " %(nilai))
else :
    print ("> nilai %s = 'F' " %(nilai))
